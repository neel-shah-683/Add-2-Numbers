a = input("Enter 1 Number: ")
b = input("Enter 2 Number: ")

# Adding 2 Numbers
c = int(a) + int(b)
print("\n Addition = ",c)

# Subtracting 2 Numbers
c = int(a) - int(b)
print("\n Subtraction = ",c)

# Multiplicating 2 Numbers
c = int(a) * int(b)
print("\n Multiplication = ",c)

# Dividing 2 Numbers
c = int(a) / int(b)
print("\n Division = ",c)

# Modulous of 2 Numbers
c = int(a) % int(b)
print("\n Modulous = ",c)